    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
        ]);

        $sistema = new Sistema([
            'name' => $request->get('name'),
            'description' => $request->get('description'),
            'app_ip' => $request->get('app_ip'),
            'app_user' => $request->get('app_user'),
            'app_pass' => $request->get('app_pass'),
            'db_ip' => $request->get('db_ip'),
            'db_user' => $request->get('db_user'),
            'db_pass' => $request->get('db_pass'),
        ]);

        $sistema ->save();

        return redirect('/sistema')->with('success', 'Adicionado com sucesso');
    }

